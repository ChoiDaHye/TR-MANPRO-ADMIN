    void harga_tambah(float h1, float h2, float h3) {
        try {
            boolean cek = false;
            dao_awal dao = new dao_awal();
            m_harga gs = new m_harga();

            for (int i = 0; i < 3; i++) {
                if (i == 0) {
                    gs.setNama("Sewa VCD");
                    gs.setHarga(h1);
                    cek = dao.buat_harga_awal(gs);
                } else if (i == 1) {
                    gs.setNama("Denda Keterlambatan");
                    gs.setHarga(h2);
                    cek = dao.buat_harga_awal(gs);
                } else if (i == 2) {
                    gs.setNama("Denda Kerusakan");
                    gs.setHarga(h3);
                    cek = dao.buat_harga_awal(gs);
                }
            }

            if (cek) {
                JOptionPane.showMessageDialog(null, "Data berhasil tersimpan!", "Berhasil", JOptionPane.INFORMATION_MESSAGE);
                Login lg = new Login();
                lg.setVisible(true);
                this.dispose();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Terjadi kesalahan saat menyimpan data!\n" + e, "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

