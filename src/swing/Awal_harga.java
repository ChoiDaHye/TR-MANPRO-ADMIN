    private void harga_btn_simpanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_harga_btn_simpanMouseClicked
        float sewa = (Float) Float.parseFloat(harga_sewa.getText());
        float terlambat = (Float) Float.parseFloat(harga_terlambat.getText());
        float rusak = (Float) Float.parseFloat(harga_rusak.getText());

        if (sewa == 0 || terlambat == 0 || rusak == 0) {
            JOptionPane.showMessageDialog(null, "Mohon lengkapi data!", "Peringatan", JOptionPane.ERROR_MESSAGE);

            if (sewa == 0) {
                harga_sewa.setText("");
                harga_sewa.requestFocus();
            } else if (terlambat == 0) {
                harga_terlambat.setText("");
                harga_terlambat.requestFocus();
            } else if (rusak == 0) {
                harga_rusak.setText("");
                harga_rusak.requestFocus();
            }
        } else {
            harga_tambah(sewa, terlambat, rusak);
        }

    }//GEN-LAST:event_harga_btn_simpanMouseClicked

