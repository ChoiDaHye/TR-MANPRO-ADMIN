    void karyawan_tampil() {
        try {
            dao_karyawan dao = new dao_karyawan();
            List<m_karyawan> data = dao.readKaryawan();

            for (m_karyawan d : data) {
                if (d.getIdKaryawan().equals(param)) {
                    edit_txt_ktp.setText(d.getNoKtp());
                    edit_txt_nama.setText(d.getNama());
                    edit_txt_kontak.setText(d.getKontak());
                    edit_txt_alamat.setText(d.getAlamat());
                    edit_cmb_level.setSelectedItem(d.getLevel());
                    iden = d.getNama().toLowerCase();
                }
            }
        } catch (Exception e) {
            System.out.println(e);
        }
    }

    void karyawan_edit(String ktp, String nama, String kontak, String alamat, String level) {
        try {
            dao_karyawan dao = new dao_karyawan();
            m_karyawan data = new m_karyawan();
            String date = new SimpleDateFormat("dd/MM/yyyy").format(new Date());

            data.setIdKaryawan(param);
            data.setNoKtp(ktp);
            data.setNama(nama);
            data.setKontak(kontak);
            data.setAlamat(alamat);
            data.setLevel(level);
            data.setEditedAt(date);

            if (dao.editKaryawan(data)) {
                JOptionPane.showMessageDialog(null, "Data berhasil tersimpan!", "Berhasil", JOptionPane.INFORMATION_MESSAGE);

                edit_txt_ktp.setText("");
                edit_txt_nama.setText("");
                edit_txt_kontak.setText("");
                edit_txt_alamat.setText("");
                edit_cmb_level.setSelectedItem(1);

                this.dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Maaf, data gagal tersimpan!", "Gagal", JOptionPane.ERROR_MESSAGE);
            }
        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, "Terjadi kesalahan saat menyimpan data!\n" + e, "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

    void karyawan_reset() {
        try {
            dao_karyawan dao = new dao_karyawan();
            m_karyawan data = new m_karyawan();
            String date = new SimpleDateFormat("dd/MM/yyyy").format(new Date());

            if (iden.contains(" ")) {
                iden = iden.substring(0, iden.indexOf(" "));
            }

            data.setIdKaryawan(param);
            data.setUsername(iden);
            data.setPassword(iden);
            data.setEditedAt(date);

            if (dao.resetKaryawan(data)) {
                JOptionPane.showMessageDialog(null, "Password berhasil diubah!", "Berhasil", JOptionPane.INFORMATION_MESSAGE);

                edit_txt_ktp.setText("");
                edit_txt_nama.setText("");
                edit_txt_kontak.setText("");
                edit_txt_alamat.setText("");
                edit_cmb_level.setSelectedItem(1);

                this.dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Maaf, password gagal diubah!", "Gagal", JOptionPane.ERROR_MESSAGE);
            }
        } catch (IOException e) {
            JOptionPane.showMessageDialog(null, "Terjadi kesalahan saat mengubah data!\n" + e, "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

