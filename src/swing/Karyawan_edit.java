    private void edit_btn_simpanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_edit_btn_simpanMouseClicked
        String ktp = edit_txt_ktp.getText(), nama = edit_txt_nama.getText(), kontak = edit_txt_kontak.getText(), alamat = edit_txt_alamat.getText(), level = edit_cmb_level.getSelectedItem().toString();

        if (ktp.isEmpty() || nama.isEmpty() || kontak.isEmpty() || alamat.isEmpty() || level.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Mohon lengkapi data!", "Peringatan", JOptionPane.ERROR_MESSAGE);

            if (ktp.isEmpty()) {
                edit_txt_ktp.requestFocus();
            } else if (nama.isEmpty()) {
                edit_txt_nama.requestFocus();
            } else if (kontak.isEmpty()) {
                edit_txt_kontak.requestFocus();
            } else if (alamat.isEmpty()) {
                edit_txt_alamat.requestFocus();
            } else if (level.isEmpty()) {
                edit_cmb_level.requestFocus();
            }
        } else {
            int selection = JOptionPane.showConfirmDialog(null, "Simpan perubahan?", "Konfirmasi", JOptionPane.OK_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE);
            if (selection == JOptionPane.OK_OPTION) {
                karyawan_edit(ktp, nama, kontak, alamat, level);
            }
        }
    }//GEN-LAST:event_edit_btn_simpanMouseClicked

    private void edit_btn_resetMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_edit_btn_resetMouseClicked
        int selection = JOptionPane.showConfirmDialog(null, "Reset password akun?", "Konfirmasi", JOptionPane.OK_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE);
        if (selection == JOptionPane.OK_OPTION) {
            karyawan_reset();
        }
    }//GEN-LAST:event_edit_btn_resetMouseClicked

